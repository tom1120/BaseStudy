public class Node {


	private Object head;
	private Object obj;
	private Object next;
	
	public Object getHead() {
		return head;
	}
	public void setHead(Object head) {
		this.head = head;
	}
	public Object getObj() {
		return obj;
	}
	public void setObj(Object obj) {
		this.obj = obj;
	}
	public Object getNext() {
		return next;
	}
	public void setNext(Object next) {
		this.next = next;
	}
	
}
